#include "libft.h"

int	ft_atoi(const char *str)
{
	int			i;
	int			cont;
	int			num;

	num = 0;
	i = 0;
	cont = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			cont++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + (str[i] - '0');
		i++;
	}
	if (cont)
		return (num * -1);
	return (num);
}
